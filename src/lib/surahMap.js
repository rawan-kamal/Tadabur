// Sorted longest-first so "آل عمران" matches before "عمران"
const SORTED_NAMES = Object.keys(SURAH_NAME_MAP).sort((a, b) => b.length - a.length)

export function extractSurahFromTitle(title) {
  if (!title) return null
  for (const name of SORTED_NAMES) {
    if (title.includes(name)) return SURAH_NAME_MAP[name]
  }
  return null
}

// Arabic ordinal words → juz number
const JUZ_NAME_MAP = {
  "الأول": 1, "الثاني": 2, "الثالث": 3, "الرابع": 4, "الخامس": 5,
  "السادس": 6, "السابع": 7, "الثامن": 8, "التاسع": 9, "العاشر": 10,
  "الحادي عشر": 11, "الثاني عشر": 12, "الثالث عشر": 13, "الرابع عشر": 14,
  "الخامس عشر": 15, "السادس عشر": 16, "السابع عشر": 17, "الثامن عشر": 18,
  "التاسع عشر": 19, "العشرون": 20, "العشرين": 20,
  "الواحد والعشرون": 21, "الثاني والعشرون": 22, "الثالث والعشرون": 23,
  "الرابع والعشرون": 24, "الخامس والعشرون": 25, "السادس والعشرون": 26,
  "السابع والعشرون": 27, "الثامن والعشرون": 28, "التاسع والعشرون": 29,
  "الثلاثون": 30, "الثلاثين": 30,
}

export function extractJuzFromTitle(title) {
  if (!title) return null

  // Pattern 1: الجزء ٢٧ or الجزء 27 (Arabic or Western digits)
  const digitMatch = title.match(/الجزء\s*([\u0660-\u0669\d]+)/)
  if (digitMatch) {
    const n = parseInt(
      digitMatch[1].replace(/[\u0660-\u0669]/g, d => d.charCodeAt(0) - 0x0660)
    )
    if (!isNaN(n) && n >= 1 && n <= 30) return n
  }

  // Pattern 2: الجزء الأول / الجزء الثاني / etc.
  for (const [word, num] of Object.entries(JUZ_NAME_MAP)) {
    if (title.includes(`الجزء ${word}`)) return num
  }

  return null
}